    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

     String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword()).trim();
 
        if (username.equals("") || password.equals("")) {
            lblStatus.setText("Please enter username and password.");
            return;
        }
 
        int userId = UserDAO.login(username, password);
 
        if (userId != -1) {
            String fullName = UserDAO.getFullName(userId);
            SessionManager.login(userId, username, fullName);  // FIXED: was SessionManager.setUser()
            new DashboardFrame().setVisible(true);
            dispose();
        } else {
            lblStatus.setText("Wrong username or password.");
            txtPassword.setText("");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

